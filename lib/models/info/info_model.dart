class InfoModel {
  final String about;
  final String avatar;
  final String linkedin;
  final String github;
  final String email;
  final String position;
  final String fullName;

  const InfoModel({
    required this.about,
    required this.avatar,
    required this.linkedin,
    required this.github,
    required this.email,
    required this.position,
    required this.fullName,
  });
}
